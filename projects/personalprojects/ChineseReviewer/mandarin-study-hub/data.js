function buildLessonsFromLegacy(source) {
  return Object.entries(source).map(([lessonId, lesson]) => {
    const enhancement = lessonEnhancements[lessonId] || {};
    const setEntries = Object.entries(lesson.sets);
    const firstSetName = setEntries[0]?.[0] || "Vocabulary";

    const cards = setEntries.flatMap(([setName, entries]) =>
      entries.map((entry) => ({
        lessonId,
        setName,
        char: entry.char,
        pinyin: entry.pinyin,
        english: entry.english,
        usage: `Use ${entry.char} in ${lesson.title}. This item comes from the set "${setName}".`,
        example: {
          hanzi: `${entry.char}`,
          pinyin: `${entry.pinyin}`,
          english: `${entry.english}`
        }
      }))
    );

    return {
      id: lessonId,
      title: lesson.title,
      topic: enhancement.topic || firstSetName,
      goals: enhancement.goals || [
        `Study the vocabulary from ${lesson.title}.`,
        `Review both lesson sets and practice fast recall.`,
        `Use the writing and grammar pages to reinforce the lesson.`
      ],
      grammarFocus: enhancement.grammarFocus || [
        { pattern: "Lesson vocabulary", simple: `Practice words from ${lesson.title} in simple sentences.` }
      ],
      sets: lesson.sets,
      cards
    };
  });
}

